package exercicio2;

public class Aviao {
	private String modelo;
	private String porte;
	private int capacidadeTripulantes;
	private int capacidadePassageiro;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getPorte() {
		return porte;
	}
	public void setPorte(String porte) {
		this.porte = porte;
	}
	public int getCapacidadeTripulantes() {
		return capacidadeTripulantes;
	}
	public void setCapacidadeTripulantes(int capacidadeTripulantes) {
		this.capacidadeTripulantes = capacidadeTripulantes;
	}
	public int getCapacidadePassageiro() {
		return capacidadePassageiro;
	}
	public void setCapacidadePassageiro(int capacidadePassageiro) {
		this.capacidadePassageiro = capacidadePassageiro;
	}
	
}

